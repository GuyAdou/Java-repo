public class Oval {
    public static void main(String[] args){
        System.out.println("Oval Area Calculator");
        
        final double PI = 3.14159;
        
        double a = 4.5;
        double b = 11.0;
        double area = (a / 2) * (b / 2) * PI;
        
        System.out.println("The area is: " + area);
        
    }
}